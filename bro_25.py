# Return Statement = Functions send Python values/objects back to the caller,
#                    These values/objects are known as the function's return value.

def sum(a, b):
    c = a + b;
    return c;
print(sum(3, 4));

def mul(d, e):
    f = d * e;
    return f;
k = mul(3, 4);
print(k);