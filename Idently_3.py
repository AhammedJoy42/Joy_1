# For Loop:
for i in range(5):
    print("Hello");
    print(i);

names = ["Joy", "Jewel", "Benny", "Agnatha"];
for name in names:
    print("Hello " + name);
    print(".....");
    print(f'Hello, {name}');
    print("....");

# While Loop:
i = 0;
while i < 3:
    print(i);
    i += 1;

# Tricky One:
a = 2;
b = 3;
print(a == b);
print(a > b);
print(a < b);
print(a >= b);
print(a <= b);
print(a != b);